import { useEffect, useState, useCallback } from "react";
import { motion, AnimatePresence } from "framer-motion";
import { Sparkles, BookOpen, Layers, FileText, CheckCircle2, Clock, Users, GraduationCap } from "lucide-react";
import { Dialog, DialogContent, DialogDescription, DialogTitle } from "@/components/ui/dialog";
import { cn } from "@/lib/utils";

interface AIGenerationLoadingDialogProps {
  open: boolean;
  courseTitle: string;
  onComplete: () => void;
}

const TICKET_SECTIONS = [
  { title: "Introduction & Overview", pages: 2, icon: BookOpen },
  { title: "Core Concepts", pages: 3, icon: Layers },
  { title: "Deep Dive & Analysis", pages: 3, icon: FileText },
  { title: "Assessment & Wrap-Up", pages: 2, icon: GraduationCap },
];

const STATUS_MESSAGES = [
  "Analyzing course intent...",
  "Building course structure...",
  "Generating content blocks...",
  "Finalizing your course...",
];

export function AIGenerationLoadingDialog({ open, courseTitle, onComplete }: AIGenerationLoadingDialogProps) {
  const [progress, setProgress] = useState(0);
  const [visibleSections, setVisibleSections] = useState(0);
  const [statusIdx, setStatusIdx] = useState(0);
  const [completed, setCompleted] = useState(false);

  const totalDuration = 5200;
  const stableOnComplete = useCallback(onComplete, [onComplete]);

  useEffect(() => {
    if (!open) {
      setProgress(0);
      setVisibleSections(0);
      setStatusIdx(0);
      setCompleted(false);
      return;
    }

    const startTime = Date.now();

    const progressInterval = setInterval(() => {
      const elapsed = Date.now() - startTime;
      const pct = Math.min((elapsed / totalDuration) * 100, 100);
      setProgress(pct);

      // Reveal sections progressively
      const sectionInterval = totalDuration / (TICKET_SECTIONS.length + 1);
      const revealed = Math.min(Math.floor(elapsed / sectionInterval), TICKET_SECTIONS.length);
      setVisibleSections(revealed);

      // Update status message
      const msgInterval = totalDuration / STATUS_MESSAGES.length;
      setStatusIdx(Math.min(Math.floor(elapsed / msgInterval), STATUS_MESSAGES.length - 1));

      if (pct >= 100) {
        clearInterval(progressInterval);
        setCompleted(true);
        setTimeout(() => stableOnComplete(), 1000);
      }
    }, 50);

    return () => clearInterval(progressInterval);
  }, [open, stableOnComplete]);

  const totalPages = TICKET_SECTIONS.reduce((s, sec) => s + sec.pages, 0);

  return (
    <Dialog open={open}>
      <DialogContent
        className="sm:max-w-sm p-0 gap-0 overflow-hidden border-0 shadow-2xl [&>button]:hidden bg-transparent"
        onPointerDownOutside={(e) => e.preventDefault()}
        onEscapeKeyDown={(e) => e.preventDefault()}
      >
        <DialogTitle className="sr-only">Generating AI Course</DialogTitle>
        <DialogDescription className="sr-only">
          Please wait while your course is being generated by AI.
        </DialogDescription>

        <div className="flex flex-col items-center">
          {/* ── Slot Machine Dispenser ── */}
          <div className="relative w-full z-10" aria-hidden="true">
            {/* Dispenser body */}
            <div className="relative mx-auto w-[85%] h-14 rounded-t-2xl bg-gradient-to-b from-muted to-muted/80 border border-border shadow-lg flex items-end justify-center overflow-hidden">
              {/* Metallic shine */}
              <div className="absolute inset-0 bg-gradient-to-r from-transparent via-background/30 to-transparent" />
              {/* Slot opening */}
              <div className="w-[75%] h-3 bg-foreground/10 rounded-t-sm border-t border-x border-border/60" />
            </div>
            {/* Side grips */}
            <div className="absolute left-[7.5%] top-2 bottom-3 w-1 rounded-full bg-border/40" />
            <div className="absolute right-[7.5%] top-2 bottom-3 w-1 rounded-full bg-border/40" />
          </div>

          {/* ── Ticket ── */}
          <motion.div
            className="relative w-[75%] -mt-1 z-0"
            initial={{ height: 0, opacity: 0 }}
            animate={{
              height: "auto",
              opacity: 1,
            }}
            transition={{ duration: 1, ease: [0.25, 0.1, 0.25, 1], delay: 0.3 }}
          >
            {/* Ticket body */}
            <div className="relative bg-card border border-border/60 rounded-b-xl overflow-hidden shadow-xl">
              {/* Perforated top edge */}
              <div className="flex justify-center gap-[6px] py-1" aria-hidden="true">
                {Array.from({ length: 20 }).map((_, i) => (
                  <div key={i} className="w-1.5 h-1.5 rounded-full bg-muted" />
                ))}
              </div>

              {/* AI Badge */}
              <div className="flex items-center justify-center gap-2 pt-2 pb-3">
                <motion.div
                  className="w-8 h-8 rounded-full bg-gradient-to-br from-primary to-[hsl(270_80%_55%)] flex items-center justify-center shadow-sm"
                  animate={completed ? { scale: [1, 1.2, 1] } : { rotate: [0, 5, -5, 0] }}
                  transition={{ duration: completed ? 0.4 : 3, repeat: completed ? 0 : Infinity }}
                >
                  {completed ? (
                    <CheckCircle2 className="w-4 h-4 text-primary-foreground" aria-hidden="true" focusable="false" />
                  ) : (
                    <Sparkles className="w-4 h-4 text-primary-foreground" aria-hidden="true" focusable="false" />
                  )}
                </motion.div>
              </div>

              {/* Course title */}
              <div className="px-5 pb-3 text-center">
                <AnimatePresence mode="wait">
                  <motion.p
                    key={completed ? "done" : "gen"}
                    initial={{ opacity: 0, y: 4 }}
                    animate={{ opacity: 1, y: 0 }}
                    exit={{ opacity: 0, y: -4 }}
                    className="text-[10px] uppercase tracking-widest text-muted-foreground font-semibold mb-1"
                  >
                    {completed ? "Course Ready" : "AI Generating"}
                  </motion.p>
                </AnimatePresence>
                <h3 className="text-sm font-bold text-foreground leading-snug line-clamp-2">
                  {courseTitle}
                </h3>
              </div>

              {/* Dotted separator */}
              <div className="mx-5 border-t border-dashed border-border/60" aria-hidden="true" />

              {/* Course stats strip */}
              <div className="flex items-center justify-around px-4 py-3">
                <div className="flex flex-col items-center gap-0.5">
                  <Layers className="w-3.5 h-3.5 text-muted-foreground" aria-hidden="true" focusable="false" />
                  <span className="text-xs font-bold text-foreground">{TICKET_SECTIONS.length}</span>
                  <span className="text-[9px] text-muted-foreground">Sections</span>
                </div>
                <div className="w-px h-6 bg-border/40" aria-hidden="true" />
                <div className="flex flex-col items-center gap-0.5">
                  <FileText className="w-3.5 h-3.5 text-muted-foreground" aria-hidden="true" focusable="false" />
                  <span className="text-xs font-bold text-foreground">{totalPages}</span>
                  <span className="text-[9px] text-muted-foreground">Pages</span>
                </div>
                <div className="w-px h-6 bg-border/40" aria-hidden="true" />
                <div className="flex flex-col items-center gap-0.5">
                  <Clock className="w-3.5 h-3.5 text-muted-foreground" aria-hidden="true" focusable="false" />
                  <span className="text-xs font-bold text-foreground">~60</span>
                  <span className="text-[9px] text-muted-foreground">Minutes</span>
                </div>
              </div>

              {/* Dotted separator */}
              <div className="mx-5 border-t border-dashed border-border/60" aria-hidden="true" />

              {/* Section reveal list — "printed" one by one */}
              <div className="px-5 py-3 space-y-1.5" role="list" aria-label="Course sections being generated">
                {TICKET_SECTIONS.map((sec, i) => {
                  const isVisible = i < visibleSections;
                  const SectionIcon = sec.icon;
                  return (
                    <motion.div
                      key={sec.title}
                      role="listitem"
                      initial={{ opacity: 0, height: 0, marginBottom: 0 }}
                      animate={isVisible ? {
                        opacity: 1,
                        height: "auto",
                        marginBottom: 6,
                      } : {}}
                      transition={{ duration: 0.5, ease: [0.25, 0.1, 0.25, 1] }}
                      className="overflow-hidden"
                    >
                      <div className={cn(
                        "flex items-center gap-2.5 px-3 py-2 rounded-lg transition-colors",
                        isVisible ? "bg-primary/[0.06]" : "bg-muted/30"
                      )}>
                        <div className="w-6 h-6 rounded-md bg-primary/15 flex items-center justify-center shrink-0">
                          <SectionIcon className="w-3 h-3 text-primary" aria-hidden="true" focusable="false" />
                        </div>
                        <div className="flex-1 min-w-0">
                          <p className="text-xs font-semibold text-foreground truncate">{sec.title}</p>
                          <p className="text-[10px] text-muted-foreground">{sec.pages} pages</p>
                        </div>
                        <motion.div
                          initial={{ scale: 0 }}
                          animate={isVisible ? { scale: 1 } : {}}
                          transition={{ delay: 0.2, type: "spring", stiffness: 300, damping: 15 }}
                        >
                          <CheckCircle2 className="w-3.5 h-3.5 text-primary" aria-hidden="true" focusable="false" />
                        </motion.div>
                      </div>
                    </motion.div>
                  );
                })}

                {/* Placeholder shimmer for unrevealed sections */}
                {!completed && visibleSections < TICKET_SECTIONS.length && (
                  <div className="space-y-1.5" aria-hidden="true">
                    {Array.from({ length: TICKET_SECTIONS.length - visibleSections }).map((_, i) => (
                      <div key={`shimmer-${i}`} className="flex items-center gap-2.5 px-3 py-2 rounded-lg">
                        <div className="w-6 h-6 rounded-md bg-muted animate-pulse" />
                        <div className="flex-1 space-y-1">
                          <div className="h-2.5 w-3/4 bg-muted animate-pulse rounded" />
                          <div className="h-2 w-1/3 bg-muted/60 animate-pulse rounded" />
                        </div>
                      </div>
                    ))}
                  </div>
                )}
              </div>

              {/* Dotted separator */}
              <div className="mx-5 border-t border-dashed border-border/60" aria-hidden="true" />

              {/* Footer: status + progress */}
              <div className="px-5 py-3 space-y-2">
                <div className="flex items-center justify-between">
                  <AnimatePresence mode="wait">
                    <motion.span
                      key={completed ? "ready" : statusIdx}
                      initial={{ opacity: 0, y: 4 }}
                      animate={{ opacity: 1, y: 0 }}
                      exit={{ opacity: 0, y: -4 }}
                      transition={{ duration: 0.2 }}
                      className="text-[11px] font-medium text-muted-foreground"
                    >
                      {completed ? "Ready to edit!" : STATUS_MESSAGES[statusIdx]}
                    </motion.span>
                  </AnimatePresence>
                  <span className="text-[11px] font-bold text-foreground tabular-nums">
                    {Math.round(progress)}%
                  </span>
                </div>

                {/* Progress bar */}
                <div className="h-1.5 w-full rounded-full bg-muted overflow-hidden">
                  <motion.div
                    className="h-full rounded-full"
                    style={{
                      background: "linear-gradient(90deg, hsl(var(--primary)), hsl(270 80% 55%), hsl(var(--primary)))",
                      backgroundSize: "200% 100%",
                    }}
                    animate={{
                      width: `${progress}%`,
                      backgroundPosition: ["0% 0%", "100% 0%", "0% 0%"],
                    }}
                    transition={{
                      width: { duration: 0.3, ease: "easeOut" },
                      backgroundPosition: { duration: 2, repeat: Infinity, ease: "linear" },
                    }}
                  />
                </div>
              </div>

              {/* Perforated bottom edge */}
              <div className="flex justify-center gap-[6px] py-1" aria-hidden="true">
                {Array.from({ length: 20 }).map((_, i) => (
                  <div key={i} className="w-1.5 h-1.5 rounded-full bg-muted" />
                ))}
              </div>
            </div>
          </motion.div>
        </div>
      </DialogContent>
    </Dialog>
  );
}
